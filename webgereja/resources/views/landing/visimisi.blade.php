    <h2>Visi dan Misi Gereja GKPI</h2>
    <br><h6 class="fw-bold">Visi :</h6>
    <p>"MENJADI PERSEKUTUAN PENYEMBAHAN DAN PERSEMBAHAN"</p>

    <br><h6 class="fw-bold">Misi :</h6>
    <p>Dalam rangka mendukung Visi dan Misi maka Misi GKPI dijabarkan dalam Panca Pelayanan GKPI:</p>
    <ol>
        <li>Koinonia (Persekutuan): GKPI terpanggil untuk membangun persekutuan sebagai bagian dari Gereja Yang Esa Kudus dan Am serta Rasuli yang dikiaskan sebagai tubus Kristus. Persekutuan itu nampak dalam persekutuan jemaat antara jemaat dan sesama dalam masyarakat, yang mencerminkan kasih Kristus</li>
        <li>Marturia (Kesaksian): GKPI terpanggil mewujudkan-nyatakan syalom Allah sebagai berita kesukaan yang utuh dan menyeluruh untuk segala mahluk dalam keutuhan ciptaan</li>
        <li>Diakonia (Pelayanan): GKPI terpanggil untuk mewujudkan-nyatakan kasih Allah kepada sesama manusia secara utuh dan menyeluruh</li>
        <li>Liturgia (Ibadah): GKPI terpanggil untuk melaksanakan “Ibadah Sejati” dalam seluruh kehidupan untuk memuliakan Allah</li>
        <li>Liturgia (Penatalayanan): GKPI terpanggil untuk melaksanakan tugas memelihara dan mengelola dunia ciptaan Allah dengan bijaksana dan adil serta bertanggungjawab kepada Allah. Dalam tugas mengelola semua ciptaan sebagai rumah tangga Allah, gereja membutuhkan sistem kelembagaan yang handal dalam rangka optimalisasi pelayanannya bagi kemuliaan Allah.</li>
    </ol>
